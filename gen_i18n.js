// Dedup on the KEY only (r[1]) so two entries with identical English text but
// different keys (e.g. buyer.orders.all 'All' vs buyer.filter.all 'All') both survive.
const seen = new Set();
const out = [];
for (const r of rows) {
  const key = r[1];
  if (seen.has(key)) continue;
  seen.add(key);
  out.push(r);
}

// ===================== SORT =====================
const catOrder = ['auth','buyer','checkout','delivery','notification','seller','shared','general'];
out.sort((a,b) => {
  const ca = catOrder.indexOf(a[0]), cb = catOrder.indexOf(b[0]);
  if (ca !== cb) return ca - cb;
  return a[1] < b[1] ? -1 : (a[1] > b[1] ? 1 : 0);
});

