// Emit public/js/i18n/en.js (canonical English) and am.js (empty Amharic map to fill).
// English value is the source string with ${...} placeholders preserved.
function jsEscape(s) {
  return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'").replace(/\n/g, '\\n').replace(/\r/g, '');
}
function buildLocale(obj, lang) {
  let s = `/* AUTO-GENERATED by gen_i18n.js — do not edit by hand.\n`;
  s += `   To add/edit translations, fill the Amharic column in i18n_terms.csv\n`;
  s += `   then run: node gen_i18n.js\n`;
  s += `   Keys map to State.t('key'). ${lang === 'am' ? 'Fill values from the CSV Amharic column.' : 'English is the canonical source.'} */\n`;
  s += `window.I18N_${lang} = {\n`;
  for (const r of out) {
    const key = r[1];
    const val = lang === 'en' ? r[2] : (r[4] === 'Yes' ? r[2] : '');
    s += `  '${key}': '${jsEscape(val)}',\n`;
  }
  s += `};\n`;
  return s;
}
